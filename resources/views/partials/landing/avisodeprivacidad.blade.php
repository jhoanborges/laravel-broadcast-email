        <p class="font-weight-bold mt-4">Uso que hacemos de la información que recabamos</p>
        Podemos usar su información personal para los siguientes fines principales, que están directamente
        relacionados con los servicios que usted solicita a Senter y con su relación legal con Senter.
         Recoger, entregar y realizar el seguimiento de envíos;
         Proveer los productos y servicios solicitados por usted (como logística, administración de la
        cadena de suministros, servicios de intermediación y despacho de aduanas, servicios financieros
        y de seguros);
         Procesar y cobrar pagos;
         Ofrecer asistencia al cliente, comunicarnos con usted y responder sus solicitudes, preguntas y
        comentarios;
         Abrir y administrar su cuenta de Senter (incluyendo su cuenta en línea en senter.mx y sus diversas
        funciones, como el Directorio y el Centro de Facturación de Senter);
         Enviar información a sus contactos, si es que usted así nos lo solicita;
         Realizar actividades de contabilidad, auditoría, facturación, reconciliación y cobranza;
         Procesar las reclamaciones que recibimos en relación con nuestros servicios, y/o
         Cumplir con la legislación vigente y con nuestras políticas.
        Además, podemos usar su información personal que recabamos para los demás fines indicados en la
        sección "Uso que hacemos de la información que recabamos" del Aviso de Privacidad, que pueden
        considerarse fines secundarios en virtud de la ley mexicana.
        No usamos, ni compartimos datos financieros o sobre pagos, salvo en relación con el procesamiento de
        pagos o cuando existan otros fundamentos legales.
        Puede revocar su consentimiento para que tratemos su información personal comunicándose con
        nosotros como se indica en la sección "Cómo ponerse en contacto con nosotros" del Aviso de Privacidad.
        Si usted revoca su consentimiento para que tratemos su información personal para alguno de los fines
        principales indicados anteriormente, es posible que en algunas situaciones no podamos ofrecer el
        servicio, aunque igualmente emprendamos determinadas tareas de procesamiento autorizadas por la ley.
        <p class="font-weight-bold mt-4">Información que compartimos</p>
        Podemos compartir su información personal según se establece en el Aviso de Privacidad, pero en la
        medida permitida por la ley, usted puede revocar su consentimiento para este uso compartido
        comunicándose con nosotros como se indica en la sección "Cómo ponerse en contacto con nosotros" del
        Aviso de Privacidad o utilizando el Centro de preferencias de privacidad.
        <p class="font-weight-bold mt-4">Solicitudes de acceso, rectificación, cancelación, oposición o revocación del consentimiento</p>
        Además de cualquier derecho otorgado en el Aviso de Privacidad, usted puede solicitarnos acceder,
        rectificar, cancelar, oponerse o revocar su consentimiento para el tratamiento de su información personal
        en la medida permitida por la ley, comunicándose con nosotros como se indica en la sección "Cómo
        ponerse en contacto con nosotros" del Aviso de Privacidad. Luego de una solicitud de cancelación válida,
        podremos retener los datos personales durante el lapso y propósitos permitidos por ley antes de
        eliminarlos.
        Responderemos su solicitud para acceder, rectificar, cancelar u oponerse al tratamiento de su información
        personal o revocar su consentimiento para que tratemos su información personal en un plazo de 20 días
        hábiles a partir de la fecha en que recibamos su solicitud completa o según lo establecido por ley. Para
        que su solicitud se considere completa, debe incluir su nombre completo y su dirección de contacto, una
        copia de su documento de identidad y una descripción detallada de su solicitud.
