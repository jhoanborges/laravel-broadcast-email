        <p class="font-weight-bold">Efectivo a partir del 14 de Marzo de 2018</p>
        En Senter Synergy S.A. de C.V. junto con nuestras empresas subsidiarias y afiliadas (en conjunto,
        "SENTER"), respetamos sus inquietudes acerca de la privacidad. Este aviso de privacidad describe los tipos
        de información personal que recabamos sobre los clientes, cómo la utilizamos y con quién podemos
        compartirla. El aviso también describe las medidas que tomamos para proteger la información personal.
        Además, le explicamos los procedimientos que tiene a su disposición para solicitarnos que (i) accedamos
        o cambiemos la información personal que tenemos sobre usted, (ii) cancelemos el consentimiento que
        anteriormente nos otorgó, (iii) no le enviemos determinadas comunicaciones, y (iv) respondamos las
        preguntas que pueda tener sobre nuestros métodos relacionados con la privacidad. Este aviso no se aplica
        a ninguna empresa subsidiaria o afiliada de Senter Synergy, S.A. de C.V. que cuente con su propio aviso
        de privacidad.
        Nuestras prácticas de privacidad pueden variar según el país o territorio en el que operamos, a fin de
        respetar las prácticas locales y los requisitos legales.
        <p class="font-weight-bold mt-4">Información que recabamos</p>
        Podemos recabar información personal de los clientes (nombre, información de contacto e información
        de pagos) en relación con varias actividades como (i) uso de los sitios web y aplicaciones de Senter, (ii)
        actividades de envío, incluida entrega y recogida de envíos, (iii) solicitudes de seguimiento de envíos o de
        respuesta a preguntas, (iv) eventos en los que participamos, y (v) promociones y otras ofertas.
        Los tipos de información personal que podemos recabar incluyen:
         Información de contacto personal y de empresa (nombre, nombre de la empresa, dirección,
        dirección de correo electrónico, número de teléfono o número de fax)
         Información de envío (como (i) información de contacto relativa al envío como nombre, dirección,
        dirección de correo electrónico y número de teléfono del remitente, (ii) firma de acuse de recibo,
        (iii) número de cuenta de Senter, e (iv) información proporcionada para ayudarnos a acceder a las
        ubicaciones a las que prestamos servicio) e información relacionada con el contenido de ciertos
        envíos, pero solo en la medida en que una persona identificable pueda estar vinculada a dicho
        contenido
         Información que nos permita verificar la identidad de una persona
         Nombres, direcciones de correo electrónico y números de teléfono de otras personas a las que se
        nos pida que enviemos información
         Información proporcionada al responder a encuestas
         Nombre de usuario, contraseña y otras formas de identificación para tener acceso a los productos
        y servicios en línea de Senter
         Controladores de redes sociales, contenidos y otros datos publicados en nuestras páginas oficiales
        en medios sociales o en cualquier parte en internet (como otras ubicaciones públicas) e
        información (como la dirección de correo electrónico y otros datos que usted permita que se
        compartan) que obtenemos a través de aplicaciones, herramientas, dispositivos y conexiones
        (incluidos los servicios de acceso de terceras partes, como el de "Entrar por Facebook") de Senter
        relacionadas con medios sociales.
         La ubicación geográfica de su dispositivo móvil si usa ciertas características de nuestras
        aplicaciones.
         Información de pago (incluida información de tarjetas de crédito o número y dirección de servicios
        de pago por Internet) e información financiera (como número de cuenta bancaria)
         Número de identificación fiscal en circunstancias en las que solicite productos o servicios para los
        que esta información sea necesaria, o en relación con ciertas promociones o sorteos
         Otra información personal que se nos pueda proporcionar para obtener un producto o servicio
        de Senter
        Cuando recogemos o entregamos un envío o prestamos otros servicios, podemos recopilar información
        sobre la ubicación física. Esta información incluye, por ejemplo, datos que nos sirvan para identificar la
        ubicación real de una dirección utilizando, por ejemplo, datos de GPS, códigos geográficos, latitud y
        longitud e imágenes de las distintas ubicaciones.
        Además, cuando visita nuestros sitios web, usa nuestras aplicaciones o interactúa con herramientas,
        dispositivos o conexiones relacionadas con Senter, podemos recabar cierta información por medios
        automáticos, como cookies y web beacons. La información que recabamos de este modo incluye:
        dirección IP, identificador único del dispositivo, características del navegador, características del
        dispositivo, sistema operativo, preferencias de idiomas, URL a las que se hace referencia, información
        sobre acciones realizadas, y fechas y horas de actividad. Una "cookie" es un archivo de texto que los sitios
        web envían a los ordenadores de los visitantes o a otros dispositivos conectados a Internet para identificar
        de forma exclusiva el navegador del visitante o para almacenar información o configuraciones en el
        navegador. Un "web beacon", también conocido como etiqueta de Internet, etiqueta de un único píxel o
        GIF transparente, vincula las páginas web o aplicaciones a servidores web y se puede utilizar para
        transmitir la información al servidor web. Por medio de estos métodos de recopilación automática,
        obtenemos y almacenamos datos de seguimiento de clics (clickstream data), que nos indican patrones de
        uso. Mientras el visitante hace clic por el sitio web, se puede estar recogiendo y almacenando un registro
        de sus acciones. Es posible que vinculemos algunos de los elementos de datos que recogemos mediante
        estos métodos automáticos, como la información de su navegador, con otra información obtenida sobre
        usted con el fin de poder conocer, por ejemplo, si ha abierto un correo electrónico que le hemos enviado.
        También es posible que utilicemos herramientas de análisis de terceros para recabar información sobre
        el tráfico de visitantes en nuestros sitios web o aplicaciones. Su navegador puede indicarle de qué forma
        avisarle cuando recibe cierto tipo de cookies o cómo restringir o deshabilitar ciertos tipos de cookies. Sin
        embargo, le rogamos que tenga en cuenta que, sin las cookies, es posible que no pueda utilizar todas las
        funciones de nuestros sitios web o aplicaciones. Tanto nosotros como otras empresas (como nuestras
        redes de publicidad) puede recabar información personal sobre las actividades y tiempo en línea de
        nuestros visitantes y a través de sitios de terceras partes cuando usen nuestros sitios web y aplicaciones.
        Los proveedores de aplicaciones, herramientas, dispositivos y conexiones de terceras partes en nuestros
        sitios web y aplicaciones, como los botones de "Me gusta" de Facebook, también pueden usar métodos
        automatizados para recabar información sobre sus interacciones con estas características. Esta
        información está sujeta a las políticas o avisos de privacidad de estos proveedores.
        <p class="font-weight-bold mt-4">Uso que hacemos de la información que recabamos</p>
        Es posible que utilicemos la información que recabamos para:
         Recoger, entregar y hacer seguimiento de los envíos
         Ofrecer los productos y servicios que usted solicita (como logística, gestión de cadena de
        suministro, servicios de intermediación y despacho de aduanas, y servicios financieros)
         Procesar y recopilar pagos
         Prestar servicio de apoyo y respuesta a los clientes y comunicarnos con usted sobre sus
        solicitudes, preguntas y comentarios
         Establecer y gestionar su cuenta de Senter (incluida su cuenta en línea de Senter.mx y sus diversas
        funciones, como agenda de direcciones y el Centro de facturación de Senter)
         Ofrecerle productos y servicios que creemos que le pueden interesar
         Comunicarle y gestionar su participación en eventos especiales, programas, encuestas, concursos,
        sorteos y otras ofertas o promociones
         Permitirle publicar en nuestros blogs y relacionarse con Senter mediante las redes sociales
         Enviar información a sus contactos si nos pide que lo hagamos
         Procesar las reclamaciones que recibimos relacionadas con nuestros servicios
         Operar, evaluar y mejorar nuestro negocio (esto incluye: desarrollo de nuevos productos y
        servicios; gestión de nuestras comunicaciones; determinación de la eficacia de nuestras ventas,
        marketing y publicidad; análisis y mejora de nuestros productos, servicios y sitios web y
        aplicaciones; y realización de actividades de contabilidad, auditoría, facturación, reconciliación y
        recopilación)
         Llevar a cabo análisis de datos (esto incluye: investigación de mercado y de consumidor, análisis
        de tendencias, análisis financiero y anonimato de la información personal)
         Identificar, prevenir y protegernos contra el fraude y otras actividades delictivas, reclamaciones y
        otras obligaciones
         Cumplir las exigencias legales correspondientes y nuestras políticas
        Además, utilizamos la información que recabamos en línea mediante cookies, web beacons y otros
        métodos automáticos para (i) personalizar las visitas de los usuarios a nuestros sitios web y aplicaciones,
        (ii) ofrecer contenido (incluso publicidad) adaptado a los intereses de los usuarios y a la forma en que
        nuestros usuarios navegan por nuestros sitios web y aplicaciones, y (iii) gestionar nuestro negocio.
        Podemos complementar los datos que recabamos mediante métodos automáticos con información sobre
        su ubicación (por ejemplo, el código postal) para ofrecerle contenido que pueda resultarle de interés.
        También utilizamos esta información para ayudarle a diagnosticar y resolver problemas técnicos,
        gestionar nuestros sitios web y aplicaciones, identificar a usuarios de nuestros sitios web y aplicaciones,
        y recabar información demográfica sobre nuestros usuarios. Utilizamos datos de seguimiento de clics para
        determinar patrones de uso y cómo podemos adaptar nuestros sitios web y aplicaciones para que se
        ajusten mejor a las necesidades de nuestros usuarios. Nuestros sitios web y aplicaciones no están
        diseñados para responder a las solicitudes de "no seguimiento" de los navegadores.
        Asimismo, es posible que hagamos otros usos de la información que recabamos sobre usted para los
        cuales le proporcionaremos un aviso específico en el momento de recabar la información.
        <p class="font-weight-bold mt-4">Publicidad basada en intereses</p>
        Es posible que, en nuestros sitios web y aplicaciones, recabemos información sobre sus actividades en
        línea y la utilicemos para ofrecerle publicidad sobre productos y servicios adaptada a sus intereses
        particulares. Esta sección de nuestra declaración de privacidad proporciona detalles y explicaciones sobre
        cómo puede hacer uso de sus opciones.
        Es posible que vea ciertos anuncios en otros sitios web porque participamos en redes de publicidad. Las
        redes publicitarias nos permiten dirigir nuestros mensajes a los usuarios mediante métodos contextuales,
        basados en intereses y demográficos. Estas redes hacen un seguimiento de sus actividades en línea
        durante un periodo de tiempo recabando información por medio de métodos automáticos, incluido el uso
        de cookies, registros de servidores web y web beacons. Las redes utilizan esta información para mostrarle
        anuncios que se ajusten a sus intereses particulares. La información que nuestras redes publicitarias
        pueden recabar incluye información sobre sus visitas a sitios web que forman parte de las redes de
        publicidad pertinentes, como las páginas o anuncios que visita y las acciones que lleva a cabo en los sitios
        web. Esta recopilación de datos tiene lugar tanto en nuestros sitios web como en los sitios web de terceros
        que forman parte de las redes publicitarias. Este proceso también nos ayuda a controlar la eficacia de
        nuestros programas de marketing.
        <p class="font-weight-bold mt-4">Información que compartimos</p>
        No vendemos ni compartimos de ningún otro modo su información personal, excepto en los casos
        descritos en esta declaración de privacidad. Para llevar a cabo nuestros servicios de recogida y entrega,
        compartimos la información de envío con terceros: transportistas, consignatarios, encargados de los
        pagos y destinatarios.
        Es posible que también compartamos la información personal que recabamos con nuestras empresas
        filiales, franquiciados, revendedores y socios de marketing. Estas entidades, que colectivamente se
        denominan aquí los "Socios Comerciales de Senter", pueden usar la información para los fines descritos
        en esta declaración de privacidad. Es posible que compartamos datos sobre la ubicación física con
        nuestros Socios Comerciales de Senter y otros terceros para, por ejemplo, mejorar los servicios basados
        en la ubicación y desarrollar mapas precisos y actualizados. Además, a menos que se oponga, podemos
        compartir otra información personal con terceros que no sean los Socios Comerciales de Senter para los
        fines de esos terceros como, por ejemplo, para ofrecer productos o servicios que le pueden interesar.
        La información recabada a través de aplicaciones, herramientas, dispositivos y conexiones de terceros
        (como la información obtenida por medio de servicios de acceso de terceros o relacionados con la forma
        en la que usa el botón de "Me gusta" de Facebook) es recopilada directamente por los proveedores de
        estas características. Esta información está sujeta a las políticas de privacidad de los proveedores de estas
        características y Senter no es responsable de las prácticas de información de dichos proveedores.
        También compartimos la información personal con terceros que prestan servicios en nuestro nombre
        según nuestras instrucciones. No autorizamos a estos terceros a utilizar o revelar la información excepto
        en los casos en que sea necesario para prestar los servicios en nuestro nombre o cumplir con las exigencias
        legales.
        Muchos de nuestros clientes nos subcontratan toda o parte de su cadena de suministro. Mediante
        nuestras unidades de negocio de envío y logística, gestionamos estas cadenas de suministro,
        centrándonos en la optimización de la cadena de suministro, expedición de carga y servicios de
        intermediación y comercio internacional para nuestros clientes de todo el mundo (incluido un amplio
        abanico de soluciones de transporte por aire, mar y tierra). También ofrecemos sistemas de tecnología de
        la información e instalaciones de distribución para cadenas de suministro exclusivas de sectores
        específicos como el cuidado de la salud, tecnología y negocios minoristas/de productos de consumo. Al
        prestar servicios de envío y logística, es posible que obtengamos, utilicemos y revelemos información
        sobre los clientes de nuestros clientes. En estas circunstancias, procesamos la información de acuerdo con
        el contrato con nuestro cliente.
        También es posible que revelemos información sobre usted (i) si así nos lo exige la ley, los reglamentos o
        algún proceso legal (como una sentencia judicial o una citación judicial), (ii) en respuesta a solicitudes de
        agencias del gobierno, como las fuerzas de seguridad y de orden público, o (iii) cuando creamos que la
        revelación es necesaria o adecuada para prevenir daños físicos o pérdidas económicas o en relación con
        una investigación de una actividad ilegal supuesta o real. Nos reservamos el derecho de transferir
        cualquier información que tengamos sobre usted en el caso de que vendamos o transfiramos todos o
        parte de nuestros negocios o activos (incluidos los casos de reorganización, disolución o liquidación).
        Acceso y correcciones
        De acuerdo con la legislación aplicable, (i) puede obtener una copia de cierta información personal que
        guardamos sobre usted, o actualizar o corregir las imprecisiones en dicha información por medio de su
        cuenta en Senter Commerce o senter.mx, o (ii) puede tener derecho a obtener acceso a la información
        personal que guardamos sobre usted poniéndose en contacto con nosotros de la forma indicada en la
        sección "Cómo ponerse en contacto con nosotros" de esta declaración de privacidad. Con el fin de ayudar
        a proteger su privacidad y mantener la seguridad, seguiremos distintos pasos para verificar su identidad
        antes de otorgarle acceso a la información. Además, si piensa que la información personal que guardamos
        sobre usted no es precisa, de acuerdo con la legislación aplicable, puede tener derecho a solicitar que la
        corrijamos o enmendemos poniéndose en contacto con nosotros de la forma indicada más adelante.
        Lugares y servicios distintos de Senter
        Para su comodidad e información, nuestros sitios web y aplicaciones pueden contener enlaces a lugares
        y servicios distintos de Senter que pueden ser operados por empresas que no están afiliadas con Senter.
        Estas compañías pueden tener sus propias políticas y declaraciones de privacidad, que le sugerimos
        encarecidamente que revise. Nuestros servicios también pueden ponerse a su disposición a través de
        plataformas de terceros (como los proveedores de tiendas de aplicaciones) o por medio de otros canales
        de terceros. No nos hacemos responsables de las prácticas de privacidad de ningún lugar o servicio distinto
        de Senter.
        <p class="font-weight-bold mt-4">Transferencia de datos</p>
        Es posible que transfiramos la información personal que recabamos sobre usted a países distintos al país
        en el que la información se recopiló originalmente. Es posible que estos países no tengan las mismas leyes
        de protección de datos que el país en el que inicialmente se recabó la información. Cuando transfiramos
        su información a otros países, la protegeremos de la forma descrita en esta declaración de privacidad y
        de acuerdo con la legislación aplicable. Nosotros utilizamos protecciones contractuales para la
        transferencia de información personal entre las distintas jurisdicciones (incluidas, por ejemplo, las
        cláusulas contractuales normativas de la Comisión Europea).
        <p class="font-weight-bold mt-4">Cómo protegemos la información personal</p>
        Mantenemos salvaguardias administrativas, técnicas y físicas diseñadas para proteger la información
        personal que nos proporciona frente a una posible destrucción, pérdida, alteración, acceso, revelación o
        uso accidental, ilegal o no autorizado. Aunque seguimos pasos para limitar el acceso a nuestras
        instalaciones y vehículos exclusivamente a las personas autorizadas para ello, la información que está
        ubicada fuera de un paquete o carta puede estar visible para otras personas.
        <p class="font-weight-bold mt-4">Actualizaciones de nuestra declaración de privacidad</p>
        Esta declaración de privacidad puede actualizarse periódicamente y sin previo aviso para reflejar los
        cambios en los métodos que seguimos en cuanto a la privacidad. Publicaremos un aviso destacado en
        nuestros sitios web para notificarle de cualquier cambio importante a nuestra declaración de privacidad
        e indicaremos en la parte superior de la declaración cuándo se produjo la actualización más reciente.
